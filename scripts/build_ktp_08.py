def render() -> str:
    total = sum(len(m["themes"]) for m in MODULES)
    if total != 85:
        raise ValueError(f"expected 85 pairs, got {total}")

    lines = [
        "# КТП: 8 класс",
        "",
        "Статус: Draft 1",
        "",
        "Канонический документ профиля. Справочник `reference/` — только для сравнения, **не редактируется**.",
        "",
        "| Параметр | Значение |",
        "|---|---|",
        "| Нагрузка | 5 ч/нед |",
        "| Учебных недель | 34 |",
        "| Пар (тем) | 85 |",
        "| Правило | 1 тема = 1 пара = 2 академических часа |",
        "",
        "Индекс КТП: [07_KTP.md](../07_KTP.md).",
        "",
        "---",
        "",
    ]
    n = 0
    for mod in MODULES:
        cnt = len(mod["themes"])
        lines.append(f"## {mod['title']} ({cnt} пар)")
        lines.append("")
        lines.append(f"**Сюжет:** {mod['scenario']}")
        if mod.get("note"):
            lines.append("")
            lines.append(f"*{mod['note']}*")
        if mod.get("module_path"):
            lines.append("")
            lines.append(f"**Материалы:** [{mod['module_path']}](../../{mod['module_path']}) (пары {mod.get('pairs', '?')})")
        lines.append("")
        lines.append("| # | Тема |")
        lines.append("|---|---|")
        for theme in mod["themes"]:
            n += 1
            lines.append(f"| {n} | {theme} |")
        lines.append("")

    lines.extend(
        [
            "---",
            "",
            "## Алгоритмический блок (обязательный)",
            "",
            "- Модуль 7 — массивы: поиск и сортировка",
            "- Модуль 8 — структуры данных (до кучи не включительно)",
            "- Модуль 9 — DP, финал: игровые задачи",
            "",
        ]
    )
    return "\n".join(lines)


def main() -> None:
    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(render(), encoding="utf-8")
    print(f"wrote {OUT}")


if __name__ == "__main__":
    main()
